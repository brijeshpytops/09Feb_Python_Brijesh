/*
In C programming, an operator is a symbol that performs an operation on one or more operands to produce a result. Operators are used to manipulate data and perform various tasks such as arithmetic operations, logical operations, assignment operations, bitwise operations, etc.

Here are some common types of operators in C:

Arithmetic Operators: Arithmetic operators are used to perform mathematical calculations such as addition (+), subtraction (-), multiplication (*), division (/), modulus (%), and increment (++) or decrement (--).

Relational Operators: Relational operators are used to compare two values. They include less than (<), greater than (>), less than or equal to (<=), greater than or equal to (>=), equal to (==), and not equal to (!=).

Logical Operators: Logical operators are used to perform logical operations on boolean values. They include logical AND (&&), logical OR (||), and logical NOT (!).

Assignment Operators: Assignment operators are used to assign values to variables. They include simple assignment (=), as well as compound assignment operators like addition assignment (+=), subtraction assignment (-=), multiplication assignment (*=), division assignment (/=), and modulus assignment (%=).

Bitwise Operators: Bitwise operators are used to perform operations at the bit level. They include bitwise AND (&), bitwise OR (|), bitwise XOR (^), bitwise NOT (~), left shift (<<), and right shift (>>).

Conditional Operator (Ternary Operator): The conditional operator (?:) is a ternary operator that evaluates a condition and returns one of two values based on whether the condition is true or false.

sizeof Operator: The sizeof operator is used to determine the size of a variable or data type in bytes.

Address (&) and Indirection () Operators: The address-of operator (&) returns the memory address of a variable, and the indirection operator () is used to access the value stored at a particular memory address.

Comma Operator: The comma operator (,) is used to separate multiple expressions within a single statement and evaluates them from left to right.
*/